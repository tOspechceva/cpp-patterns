#include <iostream>
#include <map>
using namespace std;

// –û–±—â–∞—è —á–∞—Å—Ç—å ‚Äî –∫–∞—Ä—Ç–∏–Ω–∫–∞ —Å–º–∞–π–ª–∏–∫–∞ (–æ–¥–Ω–∞ –Ω–∞ —Ç–∏–ø)
class Emoji {
    string name;
public:
    Emoji(const string& n) : name(n) {}
    void draw(int x, int y) {  // –£–Ω–∏–∫–∞–ª—å–Ω–æ–µ ‚Äî –≥–¥–µ —Ä–∏—Å—É–µ–º
        cout << name << " @ " << x << "," << y << endl;
    }
};

// –ö—ç—à ‚Äî –æ–¥–∏–Ω –æ–±—ä–µ–∫—Ç –Ω–∞ –∫–∞–∂–¥—ã–π —Å–º–∞–π–ª–∏–∫
map<string, Emoji*> emojiCache;

Emoji* getEmoji(const string& name) {
    if (emojiCache.find(name) == emojiCache.end())
        emojiCache[name] = new Emoji(name);  // –°–æ–∑–¥–∞—ë–º —Ç–æ–ª—å–∫–æ –µ—Å–ª–∏ –Ω–µ—Ç
    return emojiCache[name];
}

int main() {
    getEmoji(":)" )->draw(1, 2);  // –¢–æ—Ç –∂–µ –æ–±—ä–µ–∫—Ç
    getEmoji(":)" )->draw(3, 4);  // –†–∞–∑–Ω—ã–µ –ø–æ–∑–∏—Ü–∏–∏
    getEmoji(":(" )->draw(5, 6);
}